import { serve } from "https://deno.land/std@0.168.0/http/server.ts";
import { createClient } from "https://esm.sh/@supabase/supabase-js@2.76.1";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

interface ModerateRequest {
  response_id: string;
  content: string;
  post_id: string;
}

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { response_id, content, post_id }: ModerateRequest = await req.json();
    
    const supabaseUrl = Deno.env.get('SUPABASE_URL')!;
    const supabaseKey = Deno.env.get('SUPABASE_SERVICE_ROLE_KEY')!;
    const supabase = createClient(supabaseUrl, supabaseKey);

    console.log(`üõ°Ô∏è Moderando resposta ${response_id} do post ${post_id}`);

    // Buscar contexto do post original
    const { data: post } = await supabase
      .from('mural_posts')
      .select('content')
      .eq('id', post_id)
      .single();

    // Buscar configura√ß√£o de prompt personalizado
    const { data: settingsData } = await supabase
      .from('settings')
      .select('value')
      .eq('key', 'mural_ai_prompt_moderation')
      .single();

    const customPrompt = settingsData?.value as string;

    const LOVABLE_API_KEY = Deno.env.get('LOVABLE_API_KEY');
    if (!LOVABLE_API_KEY) {
      throw new Error('LOVABLE_API_KEY n√£o configurado');
    }

    const defaultPrompt = `Voc√™ √© o GiraBot, moderador de respostas do Mural de Pedidos de Ajuda.

POST ORIGINAL:
${post?.content}

RESPOSTA A MODERAR:
${content}

Avalie se a resposta deve ser:
1. ‚úÖ APROVADA: √ötil, respeitosa, responde ao pedido de ajuda
2. ‚ö†Ô∏è REVISAR: Tom question√°vel, pode precisar ajustes
3. ‚ùå REJEITADA: Ofensiva, spam, fora do contexto, ou desrespeitosa

Crit√©rios para aprova√ß√£o:
- Responde ao pedido de ajuda do post original
- Tem informa√ß√µes √∫teis ou conselhos pr√°ticos
- Mant√©m tom profissional e respeitoso
- N√£o cont√©m propaganda ou autopromo√ß√£o
- N√£o compartilha dados sens√≠veis de terceiros

Responda APENAS com JSON:
{
  "approved": true/false,
  "action": "approve" | "review" | "reject",
  "reason": "explica√ß√£o clara em 1 frase",
  "suggestions": "sugest√µes de ajuste (se action for 'review')" ou null
}`;

    const aiPrompt = customPrompt || defaultPrompt;

    const aiResponse = await fetch('https://ai.gateway.lovable.dev/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${LOVABLE_API_KEY}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'google/gemini-2.5-flash',
        messages: [
          { role: 'system', content: 'Voc√™ √© GiraBot, moderador do Mural. Responda APENAS com JSON v√°lido.' },
          { role: 'user', content: aiPrompt.replace('${content}', content).replace('${post?.content}', post?.content || 'Post n√£o encontrado') }
        ],
      }),
    });

    if (!aiResponse.ok) {
      console.error('AI API error:', aiResponse.status, await aiResponse.text());
      
      // Fallback: enviar para revis√£o manual
      await supabase
        .from('mural_responses')
        .update({ status: 'pending' })
        .eq('id', response_id);

      return new Response(
        JSON.stringify({ 
          approved: false,
          action: 'review',
          reason: 'Erro na modera√ß√£o IA, enviando para revis√£o manual',
          suggestions: null,
          fallback: true
        }),
        { headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
      );
    }

    const aiData = await aiResponse.json();
    const aiContent = aiData.choices?.[0]?.message?.content;
    
    if (!aiContent) {
      throw new Error('No content generated by AI');
    }

    // Extrair JSON da resposta
    const jsonMatch = aiContent.match(/\{[\s\S]*\}/);
    if (!jsonMatch) {
      throw new Error('Invalid JSON format from AI');
    }

    const result = JSON.parse(jsonMatch[0]);
    
    console.log(`üìä Modera√ß√£o: ${result.action.toUpperCase()}`);
    console.log(`üìù Raz√£o: ${result.reason}`);

    // Atualizar status da resposta baseado na modera√ß√£o
    const newStatus = result.action === 'approve' ? 'approved' : 
                      result.action === 'reject' ? 'rejected' : 'pending';

    await supabase
      .from('mural_responses')
      .update({ 
        status: newStatus,
        moderation_notes: result.reason,
        approval_source: result.action === 'approve' ? 'ai' : null,
        approved_at: result.action === 'approve' ? new Date().toISOString() : null
      })
      .eq('id', response_id);

    // Se aprovada, criar notifica√ß√£o para o autor do post
    if (result.action === 'approve') {
      try {
        const { data: postData } = await supabase
          .from('mural_posts')
          .select('author_id')
          .eq('id', post_id)
          .single();

        if (postData?.author_id) {
          await supabase
            .from('notifications')
            .insert({
              user_id: postData.author_id,
              title: 'üí¨ Nova resposta no seu post',
              message: 'Algu√©m respondeu ao seu pedido no Mural Cresci e Perdi',
              type: 'mural_response',
              is_read: false
            });

          console.log('‚úÖ Notifica√ß√£o enviada ao autor do post');
        }
      } catch (notifError) {
        console.error('Erro ao enviar notifica√ß√£o:', notifError);
        // N√£o falhar se notifica√ß√£o falhar
      }
    }

    console.log(`‚úÖ Resposta ${result.action === 'approve' ? 'aprovada' : result.action === 'reject' ? 'rejeitada' : 'em revis√£o'}: ${response_id}`);

    return new Response(
      JSON.stringify(result),
      { headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );

  } catch (error: any) {
    console.error('Error in mural-moderate-response:', error);
    return new Response(
      JSON.stringify({ 
        error: error.message,
        approved: false,
        action: 'review',
        reason: 'Erro no sistema, enviando para revis√£o manual',
        suggestions: null,
        fallback: true
      }),
      { status: 500, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );
  }
});
